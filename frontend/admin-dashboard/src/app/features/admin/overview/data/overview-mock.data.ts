const PERIOD_SCALE: Record<OverviewPeriod, number> = {
  today: 1,
  '7d': 4.2,
  '30d': 14,
  month: 18,
};

function spark(seed: number, len = 12): number[] {
  return Array.from({ length: len }, (_, i) =>
    Math.round(seed * (0.7 + Math.sin(i * 0.8 + seed) * 0.15 + i * 0.02)),
  );
}

function fmt(n: number): string {
  return n.toLocaleString('ar-SA');
}

function fmtCurrency(n: number): string {
  return `${fmt(n)} ر.س`;
}

function trendSeries(base: number, len: number, variance = 0.1): number[] {
  return Array.from({ length: len }, (_, i) =>
    Math.round(base * (1 + (i - len / 2) * 0.018 + Math.sin(i * 0.95 + base) * variance)),
  );
}

function pctSeries(base: number, len: number): number[] {
  return Array.from({ length: len }, (_, i) =>
    Number((base + Math.sin(i * 0.8) * 1.2 + i * 0.08).toFixed(1)),
  );
}

