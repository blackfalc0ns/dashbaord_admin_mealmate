type RowBase = Omit<
  CancellationRequestRow,
  | 'remainingRefundableDays'
  | 'refundableBaseKd'
  | 'cancellationFeePct'
  | 'cancellationFeeKd'
  | 'netRefundKd'
  | 'feeConfigured'
>;

function row(
  base: RowBase,
  policy: CancellationPolicy,
  feePct: number | null,
): CancellationRequestRow {
  const { remainingRefundableDays, refundableBaseKd } = computeRefundableBase(
    base.amountPaidKd,
    base.totalDays,
    base.usedDays,
    policy.operationalDeductionDays,
  );

  if (feePct === null) {
    return {
      ...base,
      remainingRefundableDays,
      refundableBaseKd,
      cancellationFeePct: null,
      cancellationFeeKd: 0,
      netRefundKd: 0,
      feeConfigured: false,
    };
  }

  const { cancellationFeeKd, netRefundKd } = applyManualCancellationFee(
    refundableBaseKd,
    feePct,
  );

  return {
    ...base,
    remainingRefundableDays,
    refundableBaseKd,
    cancellationFeePct: feePct,
    cancellationFeeKd,
    netRefundKd,
    feeConfigured: true,
  };
}

