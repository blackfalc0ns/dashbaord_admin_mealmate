role_meta = {
    "admin": ("الأدمن", "Super Admin / Country Admin — لوحة Angular", 29),
    "customer": ("العميل", "تطبيق Flutter", 26),
    "restaurant": ("المطعم", "لوحة ويب Angular", 22),
    "driver": ("المندوب", "تطبيق Flutter منفصل", 10),
}

for role, (ar, platform, count) in role_meta.items():
    folder = os.path.join(BASE, role)
    files = sorted(
        glob.glob(os.path.join(folder, "F*.md")),
        key=lambda p: int(re.search(r"F(\d+)", os.path.basename(p)).group(1)),
    )

    parts = [
        f"# {ar} — الفلو الكامل المفصّل\n\n",
        f"> **الدور:** {ar} | **المنصة:** {platform} | **الميزات:** {count}\n",
        f"> **المصدر:** دمج `workflows/{role}/F*.md` + القواعد المشتركة\n\n",
        "---\n\n",
        "## فهرس الميزات\n\n",
    ]
    for fp in files:
        name = os.path.basename(fp)
        fid = re.search(r"(F\d+)", name).group(1)
        with open(fp, encoding="utf-8") as f:
            title_line = next((l for l in f if l.startswith("# ")), "")
        title = title_line.replace("# ", "").strip()
        short = title.split("—")[-1].strip() if "—" in title else title
        short = re.sub(r"^F\d+\s*:\s*", "", short)
        parts.append(f"- **{fid}** — {short}\n")

    parts.extend(["\n---\n\n", CONNECTED_FLOWS[role], "\n## القواعد المشتركة\n\n", shared_rules, "\n\n---\n\n", "# تفاصيل كل ميزة\n\n"])
    for fp in files:
        with open(fp, encoding="utf-8") as f:
            parts.append(f.read().strip())
        parts.append("\n\n---\n\n")

    out = os.path.join(folder, "00_complete_flow_detailed.md")
    text = "".join(parts)
    with open(out, "w", encoding="utf-8") as f:
        f.write(text)
    print(f"{role}: {len(text):,} chars -> {out}")
